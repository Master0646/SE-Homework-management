package com.se.pojo;

public class TeamConfig {
		private String course_id;
		private String config_id;
		private int team_min;
		private int team_max;
		private String prefix;
		public String getCourse_id() {
			return course_id;
		}
		public void setCourse_id(String course_id) {
			this.course_id = course_id;
		}
		public String getConfig_id() {
			return config_id;
		}
		public void setConfig_id(String config_id) {
			this.config_id = config_id;
		}
		public int getTeam_min() {
			return team_min;
		}
		public void setTeam_min(int team_min) {
			this.team_min = team_min;
		}
		public int getTeam_max() {
			return team_max;
		}
		public void setTeam_max(int team_max) {
			this.team_max = team_max;
		}
		public String getPrefix() {
			return prefix;
		}
		public void setPrefix(String prefix) {
			this.prefix = prefix;
		}
		

		
}
